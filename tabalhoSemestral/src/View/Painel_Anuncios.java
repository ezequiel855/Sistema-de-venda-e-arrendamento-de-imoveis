    private void registarImoveis() {
        String sql = "insert into tbImovel(bairro,tipo_imovel,nr_casa,quarteirao,dimensao,andar,descricao,preco) values(?,?,?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            String dimensao = txtLargura.getText() + "x" + txtComprimento.getText();
            pst.setString(1, txtBairro.getText());
            pst.setString(2, cbxTipoImovel.getSelectedItem().toString());
            pst.setString(3, txtNrCasa.getText());
            pst.setString(4, txtQuarteirao.getText());
            if (cbxTipoImovel.getSelectedItem().toString().equalsIgnoreCase("Casa")) {
                pst.setString(5, dimensao);
                pst.setInt(6, 0);
            } else if (cbxTipoImovel.getSelectedItem().toString().equalsIgnoreCase("Apartamento")) {
                pst.setString(5, "null");
                pst.setInt(6, Integer.parseInt(txtAndar.getText()));
            }
            pst.setString(7, txtDescrisao.getText());
            pst.setFloat(8, Float.parseFloat(txtPreco.getText()));
            int adicionado = pst.executeUpdate();
            if (adicionado > 0) {
                idUsuario();
                idImovel();
                registarAnuncio();
            } else {
                JOptionPane.showMessageDialog(null, "Prencha correctamente os campos!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void registarAnuncio() {
        String sql = "insert into tbAnuncio(data_publicacao, tipo_anuncio,id_imovel,id_usuario) values(?,?,?,?)";
        try {
            String data = ((JTextField) jDateData.getDateEditor().getUiComponent()).getText();
            //System.out.println(data);
            pst = conexao.prepareStatement(sql);
            pst.setString(1, data);
            pst.setString(2, cbxTipoAnuncio.getSelectedItem().toString());
            pst.setInt(3, idImovel);
            pst.setInt(4, idUsuario);
            int adicionado = pst.executeUpdate();
            if (adicionado > 0) {
                JOptionPane.showMessageDialog(null, "Anúncio publicado!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void idUsuario() {
        String sql = "select idUsuario from tbUsuario where nomeUsuario=" + "'" +nomeUsuario + "'";
        
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            System.out.println(nomeUsuario);
            if (rs.next()) {
                idUsuario = rs.getInt("idUsuario");
                System.out.println(idUsuario);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        } 
    }
    
    private void idImovel() {
        String sql = "SELECT idImovel FROM tbImovel ORDER BY idImovel DESC LIMIT 1";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                idImovel = rs.getInt("idImovel");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

