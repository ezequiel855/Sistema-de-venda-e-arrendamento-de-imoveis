    private void cbxTipoImovelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cbxTipoImovelActionPerformed
        // TODO add your handling code here:
        if (cbxTipoImovel.getSelectedItem().toString().equals("Casa")) {
            setColor(pnelLargura);
            setColor(pnelComprimento);
            resetColor(pnelAndar);
        } else if (cbxTipoImovel.getSelectedItem().toString().equals("Apartamento")) {
            resetColor(pnelLargura);
            resetColor(pnelComprimento);
            setColor(pnelAndar);
        }
    }//GEN-LAST:event_cbxTipoImovelActionPerformed

    private void btnPesquisarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPesquisarMouseClicked
        // TODO add your handling code here:
        pnelRegistar.hide();
        pnelCarregarPaineis.show();
        jDesktopPaneCarregarPaineis.show();
        Pesquisar_Anuncio anuncio = new Pesquisar_Anuncio();
        anuncio.setVisible(true);
        if (jDesktopPaneCarregarPaineis.getAllFrames().length == 0) {
            jDesktopPaneCarregarPaineis.add(anuncio);
        } else {
            jDesktopPaneCarregarPaineis.remove(0);
            jDesktopPaneCarregarPaineis.add(anuncio);
            revalidate();
            repaint();
        }
    }//GEN-LAST:event_btnPesquisarMouseClicked

