    private void consultar() {
        String sql = "select nome,nomeUsuario,senha,telefone from tbUsuario where nomeUsuario=" + "'" + nomeUsuario + "'";
        try {
            pst = conexao.prepareStatement(sql);
            rs = pst.executeQuery();
            if (rs.next()) {
                txtNome.setText(rs.getString("nome"));
                txtUsuarios.setText(rs.getString("nomeUsuario"));
                txtSenha.setText(rs.getString("senha"));
                txtTelefone.setText(rs.getString("telefone"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void actualizarUsuario() {
        String sql = "UPDATE tbusuario SET nome = " + "'" + txtNome.getText() + "'" + ", nomeUsuario = " + "'" + txtUsuarios.getText() + "'" + ", senha=" + "'" + txtSenha.getText() + "'" + ", telefone=" + txtTelefone.getText() + " WHERE nomeUsuario=" + "'" + nomeUsuario + "'";
        System.out.println(sql);
        try {
            pst = conexao.prepareStatement(sql);
            //rs = pst.executeQuery();
            int adicionado = pst.executeUpdate();
            if (adicionado > 0) {
                txtNome.setText("");
                txtUsuarios.setText("");
                txtSenha.setText("");
                txtTelefone.setText("");
                JOptionPane.showMessageDialog(null, "Dados actualizados com sucesso");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

